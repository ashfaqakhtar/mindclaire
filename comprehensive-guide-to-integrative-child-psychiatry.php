                    <h6>A Holistic View on Mental Health</h6>
                    <p>Integrative psychiatry views everyone as a whole person. This means that mental health is not
                        just about chemicals in the brain but also about diet, exercise, family dynamics, and even
                        spiritual beliefs. It's like putting together a jigsaw puzzle where every piece matters. For
                        instance, did you know that gut health can significantly impact mood and cognitive function? Or
                        that mindfulness practices can improve attention and reduce anxiety? This field is all about
                        exploring these connections and using them to benefit the individual.</p>

