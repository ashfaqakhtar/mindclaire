                    <h6>The Best of Both Worlds</h6>
                    <p>One of the coolest things about integrative psychiatry is how it combines traditional medical
                        treatments with complementary therapies. Think of it as having an entire toolkit at your
                        disposal. From medications and psychotherapy to nutrition, herbal supplements, mediation and
                        yoga, it's about finding the right combination that works for each individual. It's like being a
                        mental health chef, mixing and matching ingredients to create the perfect recipe for wellness. 
                    </p>

