<script>
    document.addEventListener("DOMContentLoaded", function () {
        // Get all tab buttons and award boxes
        var tabButtons = document.querySelectorAll(".tag-button");
        var awardBoxes = document.querySelectorAll(".tag-main-box");

        // Add click event listener to each tab button
        tabButtons.forEach(function (button) {
            button.addEventListener("click", function () {
                // Get the category value of the clicked button
                var category = button.dataset.category;

                // Loop through each award box
                awardBoxes.forEach(function (box) {
                    // Check if the box belongs to the selected category or "All"
                    var boxCategories = box.dataset.category.split(",");
                    if (category === "All" || boxCategories.includes(category)) {
                        box.style.display = "block"; // Display the box
                    } else {
                        box.style.display = "none"; // Hide the box
                    }
                });

                // Remove active class from all buttons
                tabButtons.forEach(function (btn) {
                    btn.classList.remove("active-tag-btn");
                });

                // Add active class to the clicked button
                button.classList.add("active-tag-btn");
            });
        });
    });
</script>

